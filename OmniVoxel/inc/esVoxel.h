    "#version 100\n"
    "uniform mat4 view;\n"
    "uniform mat4 projection;\n"
    "uniform vec4 voxel;\n"
    "attribute vec4 position;\n"
    "attribute vec3 normal;\n"
    "attribute vec2 texcoord;\n"
    "varying float lambert;\n"
    "varying vec2 vtc;\n"
    "varying float vto;\n"
    "void main()\n"
    "{\n"
        "vtc = texcoord;\n"
        "mat4 model;\n"
        "float scale = 1.0;\n"
        "float texo = voxel.w;\n"
        "if(voxel.w < 0.0)\n"
        "{\n"
            "scale = 0.5;\n"
            "texo = -texo;\n"
        "}\n"
        "model[0] = vec4(scale, 0.0, 0.0, 0.0);\n"
        "model[1] = vec4(0.0, scale, 0.0, 0.0);\n"
        "model[2] = vec4(0.0, 0.0, scale, 0.0);\n"
        "model[3] = vec4(voxel.x, voxel.y, voxel.z, 1.0);\n"
        "mat4 modelview = view * model;\n"
        "vec4 vertPos4 = modelview * position;\n"
        "vec3 lightDir = normalize(-(vertPos4.xyz / vertPos4.w));\n"
        "vec3 vertNorm = vec3(modelview * vec4(normal, 0.0));\n"
        "lambert = max(dot(lightDir, normalize(vertNorm)), 0.0);\n"
        "vto = texo;\n"
        "gl_Position = projection * vertPos4;\n"
    "}\n";

